#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;

	if (ac > 0)
	{
		i = 1;
		while (av[i])
		{
			j = 0;
			while (av[i][j])
			{
				write (1, &av[i][j], 1);
				j++;
			}
			i++;
			write (1, "\n", 1);
		}
	}
}
